name = ""
while name != "your name":
    print("Please type your name.")
    name = input()
print("Thank you!")


# The code will keep asking the user to type their name until the user types "your name". Once the user types "your name", the program will print "Thank you!" and then end. The condition of the while loop is name != "your name", so the loop will continue to execute as long as the user has not typed "your name". The input() function call lets the user type in a string, which is stored in the name variable. The print() function call displays the string "Please type your name." to the user.